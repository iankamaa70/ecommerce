				<p>by Caitlin Hayes</p>
<div>
<p>In the not-so-distant future, our mirrors may recognize us. First thing in the morning, seeing our faces, they might turn on the music we like, let us know the weather, or give us reminders about the day ahead. With technology being developed at Cornell Tech, our mirrors might also screen for disease—by scanning our skin for changes that could indicate cancer.</p>
<p>“We wanted to find something that would benefit your life, that you could do in three seconds every morning,” says master’s degree candidate Evan Kesten ’18, Connective Media at Cornell Tech. “An easy target was skin cancer. It affects one in five Americans over the course of their lifetime, and you can tell a lot about a malignant mole or anomaly just by visual inspection. We thought we could put computer vision and machine learning technology to use to tackle this disease.”</p>
